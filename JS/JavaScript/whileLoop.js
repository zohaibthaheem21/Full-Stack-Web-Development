// while loop:

let a = 0;
while(a<6) {
    console.log(a);
    a++;
}


// sum of num:
console.log("while loop sum : ")

let b = 0;
let sum = 0;
while(b<6) {
    sum += b;
    b++;
}
console.log(sum);


// do while loop:
console.log("do while loop :");

let c = 10;

do {
    console.log(c);
    c++;
} while(c<7);